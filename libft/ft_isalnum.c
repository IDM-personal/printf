#include "libft.h"

int	ft_isalnum(int ch)
{
	int i;

	i = 0;
	if ((ch >= 48 && ch <= 57) || (ch >= 65 && ch <= 90)\
	|| (ch >= 97 && ch <= 122))
	{
		i = 1;
	}
	return (i);
}
